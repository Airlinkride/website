export function getLocationBySlug(slug: string | undefined) {
  if (!slug) {
    return undefined;
  }

  const normalizedSlug = decodeURIComponent(slug).trim().toLowerCase();

  return locations.find(
    (location) => location.slug.trim().toLowerCase() === normalizedSlug,
  );
}
